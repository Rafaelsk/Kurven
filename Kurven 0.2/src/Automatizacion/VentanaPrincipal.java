    public void autoFuzzy()
    {
        jLayeredPane1.moveToFront(graficaSalida1);
        
        jButton8.setEnabled(true);
        jMenuItem8.setEnabled(true);
        JOptionPane.showMessageDialog(this, "Kurven procedera a calcular la nueva sintonizaci√≥n mediante l√≥gica difusa, por favor espere hasta ver la gr√°fica obtenida");
        
        try
        {
            Runtime rt = Runtime.getRuntime();

            Process pr = rt.exec("./fuzzytuning datos.txt "+mk+" "+mt);
            System.out.println("./fuzzytuning datos.txt "+mk+" "+mt);
 
            BufferedReader input = new BufferedReader(new InputStreamReader(pr.getInputStream()));
 
            String line=input.readLine();
            line = line.substring(1,line.length());
            imprimirConsola("Fuzzyficando... 1/4");
            System.out.println(line);
            
            line=input.readLine();
            line = line.substring(1,line.length());
            imprimirConsola("Infiriendo... 2/4");
            System.out.println(line);
            
            line=input.readLine();
            line = line.substring(1,line.length());
            imprimirConsola("Defuzzyficando... 3/4");
            System.out.println(line);
            
            
            String xy[] = line.split(",");
            kp = xy[0];
            ki = xy[1];
            
            String aux = "Ki: "+String.format("%.2e",Float.parseFloat(ki));
            jLabel13.setText(aux);
            jLabel28.setText(aux);
            aux = "Kp: "+String.format("%.2e",Float.parseFloat(kp));
            jLabel14.setText(aux);
            jLabel29.setText(aux);
            
            imprimirConsola("Probando nuevos valores con la planta ... 4/4");
            
            Process pr2 = rt.exec("./aeulc 0.1 0.01 30 "+mk+" "+mt+" "+kp+" "+ki+" respuesta_escalon.txt");
            
            input = new BufferedReader(new InputStreamReader(pr2.getInputStream()));
            
            puntos.clear();
            
            imprimirConsola("Graficando nueva sintonizaci√≥n...");
            
            while((line=input.readLine()) != null)
            {
                xy = line.split(",");
                Punto p = new Punto(Float.parseFloat(xy[0]),Float.parseFloat(xy[1]));
                puntos.add(p);
                
                graficaSalida1.setPuntosSalida(puntos);
                graficaSalida1.graficarSalida();
            }
            
            puntos.clear();

            jLabel31.setIcon(new ImageIcon(getClass().getResource("/Automatizacion/check.png")));
            jLabel31.setText("Estos valores satisfacen el sistema de control");
            
        }
        catch(Exception e)
        {
            jLabel31.setIcon(new ImageIcon(getClass().getResource("/Automatizacion/x.png")));
            jLabel31.setText("Valores no √≥ptimos. Intente nuevos valores!");
            
            System.err.println(e.toString());
        }
    }
    
